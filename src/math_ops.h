#pragma once

// Базовые математические функции.

#include <stdint.h>

// Конвертация оригинальных float16 в float32, т.к. все бэкенды хорошо работают с float32.
float f16_to_f32(uint16_t h);

// Функция нормализации RMSNorm.
// Делит вектор на его "средний размер" (RMS), чтобы значения не росли и не затухали.
// Умножает на обучаемые веса, чтобы сохранить гибкость модели.
// Благодаря этому:
// - стабилизирует обучение
// - предотвращает взрыв/затухание значений
// - делает поведение слоёв более предсказуемым
// out - выходной вектор
//  x   - входной вектор
//  w   - обучаемый вектор весов размера n (поэлементное масштабирование)
//  n   - размер вектора
//  eps - эпсилон: маленькое число для предотвращения деления на ноль
// https://openreview.net/pdf?id=SygkZ3MTJE
void rms_norm(float *out, const float *x, const float *w, int n, float eps);

// Линейный слой (полносвязный слой): вычисляет y = W·x + b
//
// Параметры:
// y     — выходной вектор размера d_out (результат)
// W     — матрица весов размера (d_out × d_in), хранится построчно
// x     — входной вектор размера d_in
// bias  — вектор смещений размера d_out (может быть NULL, тогда без смещения)
// d_in  — размер входного вектора
// d_out — размер выходного вектора
//
// Для каждого выхода i:
//   y[i] = sum_j (W[i][j] * x[j]) + bias[i]
//
// Интуиция:
// каждая строка W — это "детектор признака", который проверяет,
// насколько вход x соответствует определённому шаблону.
void linear_layer(float *y, const float *W,
                const float *x, const float *bias,
                int d_in, int d_out);

// Softmax: преобразует вектор x в вероятностное распределение
//
// Параметры:
// x — входной вектор размера n (изменяется на месте)
// n — размер вектора
//
// Что делает:
// превращает произвольные числа (логиты) в вероятности:
//   - все значения становятся >= 0
//   - сумма всех элементов = 1
//
// Формула:
//   softmax(x_i) = exp(x_i) / sum_j exp(x_j)
//
// Численная стабильность:
// перед вычислением вычитается максимум mx,
// чтобы избежать переполнения exp.
//
// Интуиция:
// усиливает большие значения и подавляет маленькие,
// превращая "оценки" в вероятности.
void softmax(float *x, int n);

// RoPE (Rotary Positional Encoding):
// применяет позиционное кодирование к вектору v с помощью вращений.
//
// Параметры:
// v         — входной вектор (изменяется на месте), размер (n_heads × head_dim)
// pos       — позиция токена в последовательности
// n_heads   — количество голов внимания
// head_dim  — размер одной головы
// freq_base — базовая частота (обычно ~10000)
//
// Что делает:
// добавляет информацию о позиции (pos) в вектор,
// вращая пары координат (как точки на плоскости).
//
// Для каждой головы и каждой пары (i, i + head_dim/2):
//   [v_i, v_j] → поворот на угол θ = pos * freq
//
// Формула вращения:
//   v_i' = v_i * cos(θ) - v_j * sin(θ)
//   v_j' = v_i * sin(θ) + v_j * cos(θ)
//
// Интуиция:
// каждая пара координат — это точка на плоскости,
// и мы «поворачиваем» её на угол, зависящий от позиции.
// Это позволяет модели учитывать порядок токенов.
//
// Важно:
// разные координаты вращаются с разными частотами,
// что даёт многомасштабное кодирование позиции.
void rope(float *v, int pos, int n_heads, int head_dim, float freq_base);

// Функция argmax: находит индекс максимального элемента в массиве
//
// Параметры:
// v — входной массив значений (например, логиты или вероятности)
// n — размер массива
//
// Возвращает:
// индекс элемента с максимальным значением
//
// Интуиция:
// выбирает «лучший» вариант — тот, у которого наибольший вес/оценка
int argmax(const float *v, int n);